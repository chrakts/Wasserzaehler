{
    for(uint8_t i=0;i<PHASE_BINS;i++)
    {
        int16_t sinv = pgm_read_word(&sin_lut[i]);

        int16_t model =
            s->offset +
            ((int32_t)s->amplitude * sinv) / SIN_SCALE;

        s->bin[i].sum = model;
        s->bin[i].count = 1;
    }
}

void sintracker_init(
        sin_tracker_t *s,
        int16_t offset,
        int16_t amplitude)
{
    s->offset = offset;
    s->amplitude = amplitude;

    s->current_bin = 0;
    s->halfwave = 0;
    s->period_counter = 0;

    sintracker_prefill(s);
}

void sintracker_recalculate(sin_tracker_t *s)
{
    int16_t min = 32767;
    int16_t max = -32768;

    int32_t mean = 0;

    for(uint8_t i=0;i<PHASE_BINS;i++)
    {
        int16_t v =
            s->bin[i].sum /
            s->bin[i].count;

        if(v < min) min = v;
        if(v > max) max = v;

        mean += v;
    }

    s->offset = mean / PHASE_BINS;
    s->amplitude = (max - min) / 2;

    if(s->amplitude < 1)
        s->amplitude = 1;
}


uint8_t sintracker_estimate_bin(
        sin_tracker_t *s,
        int16_t sample)
{
    int16_t centered = sample - s->offset;

    int32_t norm =
        ((int32_t)centered * SIN_SCALE) /
        s->amplitude;

    if(norm > SIN_SCALE) norm = SIN_SCALE;
    if(norm < -SIN_SCALE) norm = -SIN_SCALE;

    uint8_t best = 0;
    int32_t best_err = 0x7FFFFFFF;

    for(uint8_t i=0;i<PHASE_BINS;i++)
    {
        int16_t sinv = pgm_read_word(&sin_lut[i]);

        int32_t err = sinv - norm;

        if(err < 0) err = -err;

        if(err < best_err)
        {
            best_err = err;
            best = i;
        }
    }

    return best;
}

void sintracker_process(
        sin_tracker_t *s,
        int16_t adc)
{
    int16_t centered = adc - s->offset;

    /* Nulldurchgang mit Hysterese */

    if(centered > HYSTERESIS && s->halfwave==0)
    {
        s->halfwave = 1;

        sintracker_recalculate(s);

        sintracker_prefill(s);

        s->period_counter++;
    }
    else
    if(centered < -HYSTERESIS && s->halfwave==1)
    {
        s->halfwave = 0;
    }

    /* Phase bestimmen */

    uint8_t bin =
        sintracker_estimate_bin(s, adc);

    s->current_bin = bin;

    /* Bin mitteln */

    s->bin[bin].sum += adc;
    s->bin[bin].count++;

    /* 16-fach Phase */

    s->phase16 =
        (bin * 16) / PHASE_BINS;
}

